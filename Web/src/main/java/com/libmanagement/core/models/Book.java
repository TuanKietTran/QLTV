package com.libmanagement.core.models;

public class Book {
    public String ID;
    public String Name;
    public String Author;
    public String Major;

    public Book(String ID, String name, String author, String major) {
        this.ID = ID;
        this.Name = name;
        this.Author = author;
        this.Major = major;
    }  
}
    
